#True false using if else
number = 12
#equals to
if number == 9:
    print(True)
else:
    print(False)

#less than
if number < 10:
    print(True)
else:
    print(False)
#greater than

if number > 10:
    print(True)
else:
    print(False)

#less or equals to
if number <= 10:
    print(True)
else:
    print(False)

#greater or equals t
if number >= 10:
    print(True)
else:
    print(False)


